package imcode.server ;

public class Revisits {
    
	private String revisitsId ;
	private String revisitsDate;

	public String getRevisitsId() {
	return revisitsId ;
	}
	
	public String getRevisitsDate() {
	return revisitsDate ;
	}

	public void setRevisitsId(String revisitsId) {
	this.revisitsId=revisitsId ;
	}
	
	public void setRevisitsDate(String revisitsDate) {
	this.revisitsDate=revisitsDate ;
    }

}
